        // -> 예시 : Comparable 인터페이스를 구현하면 해당 클래스가 비교 가능하여 순서지정이 가능함을 알려준다.
        // -> 주된 기능에 선택적 기능을 추가하기에 '믹스인' 이라고 한다.
