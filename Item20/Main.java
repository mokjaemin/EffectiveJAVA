        // -> 반면, 기존 클래스 위에 추상 클래스를 추가하는 것은 굉장히 어려움
        // -> 예를들어, 두 클래스의 추상 클래스를 만들 시 해당 추상 클래스는 두 클래스의 공통 조상이여야 하는데 쉽지 않음

