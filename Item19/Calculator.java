package Item19;


/**
 * 이 클래스는 계산기의 기본 기능을 제공합니다.
 */
public class Calculator {

    /**
     * 주어진 두 숫자를 더합니다.
     *
     * @param a 더할 첫 번째 숫자
     * @param b 더할 두 번째 숫자
     * @return 두 숫자의 합
     * @implSpec
     * returning the result is going to be a+b
     */
    public int add(int a, int b) {
        return a + b;
    }

}
